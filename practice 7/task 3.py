tuple = ("понедельник", "вторник", "среда", "четверг", "пятница", "суббота", "воскресенье")
n = int(input("Введите желаемое количество выходных на неделе: "))

if n == 0:
    print("Ваши рабочие дни:", ", ".join(tuple))
elif n == 7:
    print("Ваши выходные дни:", ", ".join(tuple))
else:
    print("Ваши выходные дни:", ", ".join(tuple[-n:]))
    print("Ваши рабочие дни:", ", ".join(tuple[:-n]))